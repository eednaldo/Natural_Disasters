		// control that shows state info on hover
		var info = L.control();

		info.onAdd = function (map) {
			this._div = L.DomUtil.create('div', 'info');
			this.update();
			return this._div;
		};
		
		info.update = function (props) {
			this._div.innerHTML = '<h4>US Hurricanes</h4>' +  (props ?
				'<b>' + props.name + '</b><br />' + props.qtt + ' Hurricane(s) <br>'
				: 'Hover over a state for quantity');
		};

		info.addTo(map);


		// get color depending on population density value
		function getColor(d) {
			return d > 300 ? '#800026' :
					d > 150  ? '#BD0026' :
					d > 100  ? '#E31A1C' :
					d > 50  ? '#FC4E2A' :
					d > 25   ? '#FD8D3C' :
					d > 10   ? '#FEB24C' :
					d > 1   ? '#FED976' :
								'#FFEDA0';
		}

		function style(feature) {
			return {
				weight: 2,
				opacity: 1,
				color: 'white',
				dashArray: '3',
				fillOpacity: 0.7,
				fillColor: getColor(feature.properties.qtt)
			};
		}

		function highlightFeature(e) {
			var layer = e.target;

			layer.setStyle({
				weight: 5,
				color: '#666',
				dashArray: '',
				fillOpacity: 0.7
			});

			if (!L.Browser.ie && !L.Browser.opera && !L.Browser.edge) {
				layer.bringToFront();
			}

			info.update(layer.feature.properties);
		}

		var geojson;

		function resetHighlight(e) {
			geojson.resetStyle(e.target);
			info.update();
		}

		function zoomToFeature(e) {
			map.fitBounds(e.target.getBounds(), {maxZoom: 5});
		}

		function onEachFeature(feature, layer) {
			layer.on({
				mouseover: highlightFeature,
				mouseout: resetHighlight,
				click: zoomToFeature
			});
		}

		geojson = L.geoJson(statesData, {
			style: style,
			onEachFeature: onEachFeature
		}).addTo(map);

